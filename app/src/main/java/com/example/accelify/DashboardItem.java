package com.example.accelify;

public class DashboardItem {

    private String text;
    private int imageResId;

    public DashboardItem(String text, int imageResId) {
        this.text = text;
        this.imageResId = imageResId;
    }

    public String getText() {
        return text;
    }

    public int getImageResId() {
        return imageResId;
    }
}
